// 예를 들어 출력 예시에서 첫 행 3은 앞뒤 포함 3칸
// 두번째 행의 같은 열은 값이 없는데 이 또한 세칸
// 띄어쓰기로 구분하므로 공백 + (띄어쓰기)이니까 두칸을 출력해줘야 한다